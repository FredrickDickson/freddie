import 'package:flutter/material.dart';

import '../../core/app_export.dart';
import '../../routes/app_routes.dart';
import '../../widgets/custom_bottom_bar.dart';
import './property_dashboard_initial_page.dart';

class PropertyDashboard extends StatefulWidget {
  const PropertyDashboard({Key? key}) : super(key: key);

  @override
  PropertyDashboardState createState() => PropertyDashboardState();
}

class PropertyDashboardState extends State<PropertyDashboard> {
  final GlobalKey<NavigatorState> navigatorKey = GlobalKey<NavigatorState>();
  int currentIndex = 0;

  final List<String> routes = [
    '/property-dashboard',
    '/property-search-screen',
    '/messaging-screen',
    '/user-profile-screen',
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Navigator(
        key: navigatorKey,
        initialRoute: '/property-dashboard',
        onGenerateRoute: (settings) {
          switch (settings.name) {
            case '/property-dashboard':
            case '/':
              return MaterialPageRoute(
                builder: (context) => const PropertyDashboardInitialPage(),
                settings: settings,
              );
            default:
              if (AppRoutes.routes.containsKey(settings.name)) {
                return MaterialPageRoute(
                  builder: AppRoutes.routes[settings.name]!,
                  settings: settings,
                );
              }
              return null;
          }
        },
      ),
      bottomNavigationBar: CustomBottomBar(
        currentIndex: currentIndex,
        onTap: (index) {
          if (!AppRoutes.routes.containsKey(routes[index])) {
            return;
          }
          if (currentIndex != index) {
            setState(() => currentIndex = index);
            navigatorKey.currentState?.pushReplacementNamed(routes[index]);
          }
        },
      ),
    );
  }
}
